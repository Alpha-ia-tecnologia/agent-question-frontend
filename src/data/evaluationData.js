/**
 * Função auxiliar para obter as habilidades filtradas
 */
export function getFilteredSkills(modelType, component, grade) {
    const model = evaluationData[modelType];
    if (!model || !model.components[component]) {
        return [];
    }

    const componentData = model.components[component];

    if (componentData[grade]) {
        return componentData[grade];
    }

    // Fallback: retorna todas as habilidades do componente
    const allSkills = [];
    Object.values(componentData).forEach(skills => {
        skills.forEach(skill => {
            if (!allSkills.find(s => s.code === skill.code)) {
                allSkills.push(skill);
            }
        });
    });
    return allSkills;
}

/**
 * Obtém os anos disponíveis para um modelo e componente
 */
export function getAvailableGrades(modelType, component) {
    const model = evaluationData[modelType];
    if (!model || !model.components[component]) {
        return [];
    }
    return Object.keys(model.components[component]);
}
